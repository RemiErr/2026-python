# R02. 正則表達式：搜尋、替換、旗標（2.4–2.8）
# re.compile / findall / sub / IGNORECASE / 非貪婪 / DOTALL

# ═══════════════════════════════════════════════════════════════════════════
# 正則表達式（Regular Expression, Regex）是一種強大的文字搜尋和替換工具
# 主要用途：搜尋特定的文字模式、替換內容、驗證格式等
# ═══════════════════════════════════════════════════════════════════════════

import re  # 匯入 Python 的正則表達式模組

# ═══════════════════════════════════════════════════════════════════════════
# ── 2.4 匹配和搜尋（match 和 findall）
# ═══════════════════════════════════════════════════════════════════════════

# 定義要搜尋的文字內容
text = "Today is 11/27/2012. PyCon starts 3/13/2013."

# re.compile() 用來編譯一個正則表達式模式
# r"(\d+)/(\d+)/(\d+)" 是一個正則表達式模式，意思是：
#   - \d+ 表示「一個或多個數字」
#   - / 表示文字中的斜線符號
#   - (...) 表示「捕獲群組」，會記錄括號內的匹配內容
# 整個模式意思：匹配「數字/數字/數字」的日期格式
datepat = re.compile(r"(\d+)/(\d+)/(\d+)")

# findall() 找到所有符合模式的內容，並以列表形式回傳
# 因為有 3 個括號（群組），所以每個匹配會回傳一個包含 3 個元素的元組
print(datepat.findall(text))
# 輸出：[('11', '27', '2012'), ('3', '13', '2013')]
# 解釋：找到了 2 個日期，分別是 11/27/2012 和 3/13/2013

# match() 嘗試在字串開頭匹配，如果匹配成功回傳一個 Match 物件，失敗則回傳 None
m = datepat.match("11/27/2012")  # 在開頭匹配成功
assert m is not None  # 用 assert 確保 m 不是空值（不是 None），否則程式會停止
print(m.group(0), m.groups())  # 輸出：'11/27/2012' ('11', '27', '2012')
# 解釋：
#   - m.group(0) 回傳整個匹配的內容
#   - m.groups() 回傳所有捕獲群組的內容（括號內的部分）

# finditer() 逐個找出所有符合的內容，回傳一個迭代器（可以用 for 迴圈逐一處理）
for m in datepat.finditer(text):
    month, day, year = m.groups()  # 將 3 個群組分別解包到 month、day、year
    print(f"{year}-{month}-{day}")  # 把日期格式轉換為 YYYY-MM-DD
# 輸出：
# 2012-11-27
# 2013-3-13

# ═══════════════════════════════════════════════════════════════════════════
# ── 2.5 搜尋和替換（sub 和 subn）
# ═══════════════════════════════════════════════════════════════════════════

# re.sub() 用來替換符合模式的內容
# 第 1 個參數：正則表達式模式
# 第 2 個參數：替換成的內容（可以用 \1, \2, \3... 指代捕獲的群組）
#   - \3 指代第 3 個括號內的內容（年份）
#   - \1 指代第 1 個括號內的內容（月份）
#   - \2 指代第 2 個括號內的內容（日期）
# 第 3 個參數：要搜尋替換的字串
print(re.sub(r"(\d+)/(\d+)/(\d+)", r"\3-\1-\2", text))
# 輸出：'Today is 2012-11-27. PyCon starts 2013-3-13.'
# 解釋：所有的日期都被轉換成 YYYY-MM-DD 格式

# ─ 使用命名群組的方式（更容易閱讀和理解）
# (?P<name>...) 語法用來給一個群組取一個有意義的名字
print(
    re.sub(
        r"(?P<month>\d+)/(?P<day>\d+)/(?P<year>\d+)",  # 定義三個有名稱的群組
        r"\g<year>-\g<month>-\g<day>",  # 用群組名稱來引用（\g<name> 語法）
        text,
    )
)
# 輸出：'Today is 2012-11-27. PyCon starts 2013-3-13.'
# 解釋：效果和上面一樣，但使用有名稱的群組讓代碼更容易理解

# re.subn() 和 re.sub() 功能一樣，但會額外回傳「替換的次數」
# 回傳值是一個元組：(新字串, 替換次數)
newtext, n = re.subn(r"(\d+)/(\d+)/(\d+)", r"\3-\1-\2", text)
print(f"替換了 {n} 次")  # 輸出：替換了 2 次
# 解釋：在 text 中找到了 2 個日期並成功替換

# ═══════════════════════════════════════════════════════════════════════════
# ── 2.6 忽略大小寫（re.IGNORECASE 旗標）
# ═══════════════════════════════════════════════════════════════════════════

# 定義含有不同大小寫的字串
s = "UPPER PYTHON, lower python, Mixed Python"

# flags=re.IGNORECASE 參數用來忽略大小寫差異
# 即使搜尋的是 "python"（小寫），也會匹配 "PYTHON"、"python"、"Python" 等
print(re.findall("python", s, flags=re.IGNORECASE))
# 輸出：['PYTHON', 'python', 'Python']
# 解釋：找到了 3 個 "python"（不管大小寫），並回傳原始文字的樣子

# ═══════════════════════════════════════════════════════════════════════════
# ── 2.7 非貪婪 vs 貪婪（最短匹配 vs 最長匹配）
# ═══════════════════════════════════════════════════════════════════════════

# 定義含有引號的字串
text2 = 'Computer says "no." Phone says "yes."'

# 貪婪模式：.* 會匹配盡可能長的內容（從第一個 " 一直到最後一個 "）
print(re.compile(r'"(.*)"').findall(text2))
# 輸出：['no." Phone says "yes.']
# 解釋：貪婪匹配一直往右走到最後一個引號，所以捕獲了太多東西

# 非貪婪模式：.*? 會匹配盡可能短的內容（*? 中的 ? 表示「非貪婪」）
# 這樣會在遇到第一個 " 時就停止
print(re.compile(r'"(.*?)"').findall(text2))
# 輸出：['no.', 'yes.']
# 解釋：非貪婪匹配正確地捕獲了兩個引號內的內容

# 重點：在 .* 後面加上 ? 就變成非貪婪匹配
# 這在處理需要「找到最近的對應符號」的情況時很有用

# ═══════════════════════════════════════════════════════════════════════════
# ── 2.8 多行匹配（re.DOTALL 旗標）
# ═══════════════════════════════════════════════════════════════════════════

# 定義一個包含換行符的字串（跨越多行的內容）
code = "/* this is a\nmultiline comment */"
# \n 是換行符號，所以這個字串看起來像：
# /* this is a
# multiline comment */

# 正常情況下，. 符號在正則表達式中只能匹配「除了換行符以外」的任何字符
# 加上 re.DOTALL 旗標後，. 就可以匹配包含換行符的任何字符
print(re.compile(r"/\*(.*?)\*/", re.DOTALL).findall(code))
# 輸出：[' this is a\nmultiline comment ']
# 解釋：
#   - /\* 匹配開頭的 /*
#   - (.*?) 非貪婪地匹配中間的所有內容（包括換行符，因為有 re.DOTALL）
#   - \*/ 匹配結尾的 */

# 如果沒有 re.DOTALL，. 會遇到 \n 時就停止，所以無法正確匹配跨行的內容

# ═══════════════════════════════════════════════════════════════════════════
# 總結：正則表達式的關鍵概念
# ═══════════════════════════════════════════════════════════════════════════
# 1. re.compile() - 編譯正則表達式模式
# 2. findall() - 找出所有符合的內容
# 3. match() - 在開頭匹配
# 4. finditer() - 逐一返回匹配的物件
# 5. sub() - 替換符合的內容
# 6. \d, \w, \s - 代表數字、字母數字、空白
# 7. (...) - 捕獲群組，可以用 \1, \2 等引用
# 8. * 表示 0 個或多個，+ 表示 1 個或多個
# 9. 用 ? 使 *, +, {n,m} 變成非貪婪
# 10. re.IGNORECASE, re.DOTALL 等旗標可以改變匹配行為
# ═══════════════════════════════════════════════════════════════════════════
