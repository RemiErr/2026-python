# U1. 解包失敗的原因：變數數量 ≠ 元素數量（1.1）

# 建立一個包含 2 個數字的元組（tuple）
# 元組是一個有序的資料集合，用括號 () 表示，裡面的元素用逗號分隔
p = (4, 5)

# 下面這行程式會發生錯誤（ValueError），因為：
# - 左邊有 3 個變數要接收：x, y, z
# - 右邊的元組 p 只有 2 個元素：4, 5
# - 數量不匹配，所以 Python 會拋出錯誤訊息
#
# 解包（unpacking）的規則：變數個數必須 = 元素個數
# 例如：如果元組有 2 個元素，就只能用 2 個變數來接收
#
# x, y, z = p  # ❌ ValueError：元素只有 2 個但變數要 3 個

# 正確的解包方式範例：
# x, y = p  # ✅ 這樣就可以了！x 會得到 4，y 會得到 5
