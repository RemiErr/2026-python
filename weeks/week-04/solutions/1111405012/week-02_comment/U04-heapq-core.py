# U4. heap 為何能高效拿 Top-N（1.4）
# 說明：這個程式展示 Python 中 heapq（堆積隊列）模組的基本使用
#      Heap 是一種特殊的樹狀結構，能快速找到最小值（或最大值）

import heapq  # 導入 heapq 模組，用於操作堆積隊列

# 建立一個普通的列表，包含 4 個數字
nums = [5, 1, 9, 2]

# 複製 nums 列表到 h，使用 [:] 表示複製整個列表
# 為什麼要複製？因為 heapify() 會直接修改列表，所以先複製避免改動原始資料
h = nums[:]  # h = [5, 1, 9, 2]

# 將列表 h 轉換成堆積結構（heap structure）
# heapify() 會重新排列元素，使其滿足「小堆積」的性質：
# - 父節點的值 ≤ 子節點的值
# - 特別是 h[0] 永遠是最小值
heapq.heapify(h)  # h 現在已轉換為 heap，h[0] 是最小值

# h[0] 永遠是最小值（這是 heap 的核心性質）
# 註解說明 heap 的關鍵特性

# 從堆積中取出（彈出）最小的元素
# heappop() 會：
#   1. 取出 h[0]（最小值）
#   2. 移除這個元素
#   3. 重新調整 heap 結構，保證新的 h[0] 是次小的值
m = heapq.heappop(h)  # 每次 pop 都拿到目前最小

# 此時 m = 1（原列表中的最小值）
# h 變成 [2, 5, 9]（剩下的元素，已重新調整成 heap）
