# U8. 字典最值為何常用 zip(values, keys)（1.8）
# 本範例展示在字典中找最小/最大值時，如何同時取得對應的 key

# 建立一個字典：存放商品名稱和價格
# 'A' 的價格是 2.0 元，'B' 的價格是 1.0 元
prices = {'A': 2.0, 'B': 1.0}

# 方法 1：直接對字典使用 min()
# 結果：回傳 'A'（因為它按字母序比 'B' 小）
# 問題：這回傳的是「key 的最小值」，不是「price 的最小值」
min(prices)            # 結果是 'A'

# 方法 2：只找最小的價格（value）
# 結果：回傳 1.0（B 的價格最便宜）
# 問題：你知道最便宜的價格是 1.0，但不知道是哪個商品
min(prices.values())   # 結果是 1.0

# 方法 3：使用 zip() 同時取得最小價格「和」對應的商品名稱（推薦做法）
# zip(prices.values(), prices.keys()) 把價格和商品名稱配對起來
# 例如：[(2.0, 'A'), (1.0, 'B')]
# min() 會比較第一個元素（價格），找出最小的那一對
# 結果：回傳 (1.0, 'B')，一次拿到「最便宜的價格」和「商品名稱」
min(zip(prices.values(), prices.keys()))
# 結果是 (1.0, 'B')
